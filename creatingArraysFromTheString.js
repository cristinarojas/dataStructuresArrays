// split() is a function of the string - this function breaks up a string at a common
// delimiter.
 var sentence = "Cristina Rojas Elizabeth Rojas Zamora is my name";

 var words = sentence.split(" "); // Create an array from the string

console.log(words); // ["Cristina", "Rojas", "Elizabeth", "Rojas", "Zamora", "is", "my", "name"]
